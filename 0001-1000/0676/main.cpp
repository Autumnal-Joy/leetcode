class MagicDictionary {
    vector<string> dict;

public:
    MagicDictionary() = default;

    void buildDict(const vector<string> &dictionary) {
        dict = dictionary;
    }

    static bool check(const string &word, const string &target) {
        if (word.size() != target.size()) return false;
        auto cnt = 0;
        for (size_t i = 0; i < word.size(); ++i) {
            if (word[i] != target[i]) {
                ++cnt;
            }
            if (cnt > 1) {
                return false;
            }
        }
        return cnt == 1;
    }

    bool search(const string &searchWord) {
        return ranges::any_of(dict, [&](const auto &word) {
            return check(word, searchWord);
        });
    }
};

