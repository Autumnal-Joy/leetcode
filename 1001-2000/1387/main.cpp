class Solution {

public:
    int getKth(const int lo, const int hi, const int k) {
        auto id = vector<int>(hi - lo + 1);
        std::iota(id.begin(), id.end(), 0);
        ranges::sort(id, [&](auto a, auto b) {
            return dp[a + lo] == dp[b + lo] ? a < b : dp[a + lo] < dp[b + lo];
        });
        return id[k - 1] + lo;
    }
};

