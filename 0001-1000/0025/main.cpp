/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode *reverseKGroup(ListNode *head, const int k) {
        if (k == 1) return head;
        auto reverse = [](ListNode *node, int k) {
            auto header = ListNode(0);
            while (k--) {
                const auto temp = node->next;
                node->next = header.next;
                header.next = node;
                node = temp;
            }
            return header.next;
        };

        auto header = ListNode(0, head);
        auto prev = &header;
        auto begin = head, end = head;
        while (true) {
            for (int i = 0; i < k; ++i) {
                if (!end) return header.next;
                end = end->next;
            }
            prev->next = reverse(begin, k);
            begin->next = end;
            prev = begin;
            begin = end;
        }
    }
};

