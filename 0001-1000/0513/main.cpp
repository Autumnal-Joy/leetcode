/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int findBottomLeftValue(TreeNode *root) {
        if (!root) return {};
        auto res = 0;
        auto layer = deque<TreeNode *>();
        layer.push_back(root);
        while (!layer.empty()) {
            res = layer.front()->val;
            auto n = layer.size();
            while (n--) {
                const auto back = layer.back();
                layer.pop_back();
                if (back->right) {
                    layer.push_front(back->right);
                }
                if (back->left) {
                    layer.push_front(back->left);
                }
            }
        }
        return res;
    }
};

