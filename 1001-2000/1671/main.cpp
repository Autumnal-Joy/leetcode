class Solution {
public:
    int minimumMountainRemovals(vector<int> &nums) {
        const auto n = static_cast<int>(nums.size());
        auto dp1 = vector(n, 0);
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < i; ++j) {
                if (nums[j] < nums[i]) {
                    dp1[i] = std::max(dp1[i], dp1[j]);
                }
            }
            ++dp1[i];
        }
        ranges::reverse(nums);
        auto dp2 = vector(n, 0);
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < i; ++j) {
                if (nums[j] < nums[i]) {
                    dp2[i] = std::max(dp2[i], dp2[j]);
                }
            }
            ++dp2[i];
        }
        auto res = 0;
        for (int i = 1; i < n - 1; ++i) {
            if (dp1[i] > 1 && dp2[n - 1 - i] > 1) {
                res = std::max(res, dp1[i] + dp2[n - 1 - i] - 1);
            }
        }
        return n - res;
    }
};

