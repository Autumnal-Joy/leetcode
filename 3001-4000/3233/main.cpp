#define N 40000

auto isPrime = bitset<N + 1>();

auto init = [] {
    isPrime.set();
    isPrime.reset(0);
    isPrime.reset(1);
    for (int i = 2; i <= N; ++i) {
        if (isPrime[i]) {
            for (int j = i * i; j <= N; j += i) {
                isPrime.reset(j);
            }
        }
    }
    return 0;
}();

class Solution {
public:
    int nonSpecialCount(const int l, const int r) {
        const int begin = static_cast<int>(std::sqrt(l - 1)) + 1;
        const int end = static_cast<int>(std::sqrt(r));
        auto mask = bitset<N + 1>();
        for (int i = begin; i <= end; ++i) {
            mask.set(i);
        }
        return r - l + 1 - static_cast<int>((mask & isPrime).count());
    }
};

