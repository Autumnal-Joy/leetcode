class Solution {
public:
    vector<int> shortestDistanceAfterQueries(const int n, const vector<vector<int>> &queries) {
        auto fa = vector<int>(n - 1);
        std::iota(fa.begin(), fa.end(), 0);
        function<int(int)> find = [&](const int x) {
            if (fa[x] != x) {
                fa[x] = find(fa[x]);
            }
            return fa[x];
        };
        auto dis = n - 1;
        auto res = vector<int>();
        res.reserve(queries.size());
        for (const auto &query: queries) {
            auto bridge1 = find(query[0]);
            const auto bridge2 = find(query[1] - 1);
            while (bridge1 != bridge2) {
                fa[bridge1] = bridge2;
                bridge1 = find(bridge1 + 1);
                --dis;
            }
            res.push_back(dis);
        }
        return res;
    }
};

