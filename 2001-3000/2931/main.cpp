class Solution {
    using PLI = pair<long long, int>;

public:
    long long maxSpending(const vector<vector<int>> &values) {
        const auto m = static_cast<int>(values.size());
        const auto n = static_cast<int>(values[0].size());
        auto pq = priority_queue<PLI, vector<PLI>, std::greater<>>();
        for (int i = 0; i < m; ++i) {
            pq.emplace(values[i][n - 1], i);
        }
        auto idx = vector(m, n - 2);
        auto res = 0ll;
        for (int i = 1; i <= m * n; ++i) {
            const auto [val, id] = pq.top();
            pq.pop();
            res += val * i;
            if (idx[id] >= 0) {
                pq.emplace(values[id][idx[id]--], id);
            }
        }
        return res;
    }
};

