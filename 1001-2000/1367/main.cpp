/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
    bool dfs(const ListNode *head, const TreeNode *root, const ListNode *cur) {
        if (!cur) return true;
        if (!root) return false;
        auto res = false;
        if (root->val == cur->val) {
            res = dfs(head, root->left, cur->next) ||
                  dfs(head, root->right, cur->next);
        }
        if (cur == head) {
            res = res || dfs(head, root->left, head) ||
                  dfs(head, root->right, head);
        }
        return res;
    }

public:
    bool isSubPath(const ListNode *head, const TreeNode *root) {
        return dfs(head, root, head);
    }
};

