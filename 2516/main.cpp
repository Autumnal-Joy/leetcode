class Solution {
public:
  int takeCharacters(string s, int k) {
    vector<deque<int>> loc(3);
    for (int i = 0; i < s.size(); i++) {
      loc[s[i] - 'a'].push_back(i);
    }
    if (loc[0].size() < k || loc[1].size() < k || loc[2].size() < k) {
      return -1;
    }
    int minCost = std::numeric_limits<int>::max();
    auto cnt = vector<int>(3);
    for (int leftCost = 0; leftCost < s.size(); leftCost++) {
      for (int i = 0; i < 3; i++) {
        cnt[i] = static_cast<int>(
            std::lower_bound(loc[i].begin(), loc[i].end(), leftCost) -
            loc[i].begin());
      }
      int rightCost = 0;
      for (int i = 0; i < 3; i++) {
        if (k > cnt[i]) {
          rightCost = std::max(
              rightCost, static_cast<int>(
                             s.size() - loc[i][loc[i].size() - (k - cnt[i])]));
        }
      }
      minCost = std::min({minCost, leftCost + rightCost});
    }
    return minCost;
  }
};

